  dim_colors      = c(locomotion   = "#2E7D32",   # vert
                      diet         = "#C62828",   # rouge
                      reproduction = "#1565C0"),  # bleu
  null_fill       = "grey85",
  null_border     = "grey45",
  pval_text_size  = 2.4,
  
  dim_order      = c("locomotion", "diet", "reproduction"),
  dim_labels     = c(locomotion   = "Locomotion",
                     diet         = "Diet",
                     reproduction = "Reproduction"),
  scenario_order = c("IUCN 100", "IUCN AT"),
  
  # Labels et codes internes pour chaque ligne
