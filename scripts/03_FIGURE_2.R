# Figure 2 (compact version): Forecasted functional changes
#
#   3 x 3 grid:

#                   Change in functional uniqueness
#     - columns : Locomotion / Diet / Reproduction
#     - within each panel: IUCN 100 (left) | IUCN AT (right) side by side
#
#   Sign conventions:
#     - Species change  : negative (species loss)
#     - FRic change     : negative (functional richness loss)
#     - FUn change      : positive if gain (surviving species more isolated)
#
#   Empirical  : coloured boxplot (stochastic) or horizontal line (deterministic)
#   Null       : grey violin
#   Annotation : SES + p-value (except Species change: no SES by construction)
#
# Depends on extinction_results generated by 02_ANALYSE_ExtinctionScenario.R
# (with $ses_stats per dimension).
#
# Author : A. Toussaint
#-------------------------------------------------------------------------------


